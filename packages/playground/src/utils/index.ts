/**
 * F:F
 * 
 * @title 求两数相加
 * @description 这是一个求两数相加的函数
 * @param {number} a 加数1
 * @param {number} b 加数2
 */
function add(a, b) {
    return a + b;
}

/**
 * F:F
 * 
 * @title 求两数相乘
 * @description 这是一个求两数相乘的函数
 * @param {number} a 乘数1
 * @param {number} b 乘数2
 */
 function multi(a, b) {
    return a * b;
}